            Connection con1=MyDbConnection.getConnection();
            
            JasperDesign Jdesign =JRXmlLoader.load("D:\\NetBeans\\Container\\Container\\Hit\\src\\View\\Deploy.jrxml");
            String query="select * from deploycontainers";
            
            JRDesignQuery updateQuery=new JRDesignQuery();
            updateQuery.setText(query);
            
