    private void btn_returnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_returnActionPerformed
        if(txt_id.getText().trim().isEmpty())
        {
            err_id.setText("Id cannot be empty");
            err_id.setForeground(Color.red);
        }
        if(txt_registration.getText().trim().isEmpty())
        {
            err_regi.setText("Registration no cannot be empty");
            err_regi.setForeground(Color.red);
        }
        if(txt_name.getText().trim().isEmpty())
        {
            err_name.setText("Name cannot be empty");
            err_name.setForeground(Color.red);
        }
        if(dob_returndate.getDate()==null)
        {
            err_date.setText("Date cannot be empty");
            err_date.setForeground(Color.red);
        }
        if(txt_delay.getText().trim().isEmpty())
        {
            err_penalty.setText("Delay penalty cannot be empty");
            err_penalty.setForeground(Color.red);
        }
        if(txt_final.getText().trim().isEmpty())
        {
            err_price.setText("Fianl price cannot be empty");
            err_price.setForeground(Color.red);
        }
        else
        {String id,regi,name,date,price;
        int penalty;
        id=txt_id.getText();
        regi=txt_registration.getText();
        name=txt_name.getText();
        Date date1=dob_returndate.getDate();
        DateFormat formate1=new SimpleDateFormat("yyy-MM-dd");
        date=formate1.format(date1);
        penalty=Integer.parseInt(txt_delay.getText());
        price=txt_final.getText();
        
        CReturnContainers w=new CReturnContainers();
        w.returnData1(id,regi,name,date,penalty,price);
        
        }
    }//GEN-LAST:event_btn_returnActionPerformed

